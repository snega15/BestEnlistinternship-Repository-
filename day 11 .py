#MERGE TWO LIST INTO TUPLE USING ZIP FUNCTION

def merge(list1,list2):
    merged_list=tuple(zip(list1,list2))
    return merged_list
list1=[1,2,3]
list2=['a','b','c']
print(merge(list1,list2))


# RANGE FROM 1 TO 8.AND MERGE

